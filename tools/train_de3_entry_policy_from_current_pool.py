def main() -> None:
    parser = argparse.ArgumentParser(
        description="Train fresh DE3 entry-policy candidates from the current-pool export."
    )
    parser.add_argument(
        "--base-bundle",
        default=str(((CONFIG.get("DE3_V4") or {}).get("bundle_path") or "").strip()),
        help="Base DE3 bundle that supplies the router/lane inventory and receives the new entry-policy model.",
    )
    parser.add_argument(
        "--decisions-csv",
        default="reports/de3_current_pool_2011_2024.csv",
        help="Current-pool decisions CSV.",
    )
    parser.add_argument(
        "--trade-attribution-csv",
        default="reports/de3_current_pool_2011_2024_trade_attribution.csv",
        help="Current-pool trade-attribution CSV.",
    )
    parser.add_argument(
        "--output-dir",
        default="artifacts/de3_entry_policy_current_pool",
        help="Directory for candidate bundles and summary files.",
    )
    parser.add_argument(
        "--only",
        default="",
        help="Optional comma-separated candidate names to train.",
    )
    args = parser.parse_args()

    base_bundle_path = _resolve_path(str(args.base_bundle))
    decisions_csv_path = _resolve_path(str(args.decisions_csv))
    trade_csv_path = _resolve_path(str(args.trade_attribution_csv))
    output_dir = _resolve_output_dir(str(args.output_dir))

    base_bundle = json.loads(base_bundle_path.read_text(encoding="utf-8"))
    split_summary = _extract_split_summary(base_bundle)
    variants = _extract_variants(base_bundle)
    base_training_cfg = copy.deepcopy(
        ((CONFIG.get("DE3_V4") or {}).get("training") or {})
    )
    if not isinstance(base_training_cfg, dict) or not isinstance(base_training_cfg.get("entry_policy"), dict):
        raise SystemExit("CONFIG['DE3_V4']['training']['entry_policy'] is missing.")

    dataset = {
        "metadata": {
            "decisions_csv_path": str(decisions_csv_path),
            "trade_attribution_csv_path": str(trade_csv_path),
            "router_model_or_router_rules": copy.deepcopy(
                base_bundle.get("router_model_or_router_rules", {})
            )
            if isinstance(base_bundle.get("router_model_or_router_rules", {}), dict)
            else {},
            "lane_variant_quality": copy.deepcopy(base_bundle.get("lane_variant_quality", {}))
            if isinstance(base_bundle.get("lane_variant_quality", {}), dict)
            else {},
        },
        "split_summary": dict(split_summary),
        "variants": list(variants),
    }

    profiles = _candidate_profiles()
    if str(args.only or "").strip():
        allowed = {item.strip() for item in str(args.only).split(",") if item.strip()}
        profiles = {name: value for name, value in profiles.items() if name in allowed}
        if not profiles:
            raise SystemExit("No candidate profiles matched --only.")

    summary = {
        "created_at_utc": datetime.now(timezone.utc).isoformat(),
        "base_bundle_path": str(base_bundle_path),
        "decisions_csv_path": str(decisions_csv_path),
        "trade_attribution_csv_path": str(trade_csv_path),
        "split_summary": dict(split_summary),
        "variant_count": int(len(variants)),
        "candidates": {},
    }

    for name, overrides in profiles.items():
        candidate_cfg = copy.deepcopy(base_training_cfg)
        _deep_merge(candidate_cfg, copy.deepcopy(overrides))
        result = train_de3_v4_entry_policy(dataset=dataset, cfg=candidate_cfg)
        decision_result = train_de3_v4_decision_policy(dataset=dataset, cfg=candidate_cfg)
        entry_model = result.get("entry_policy_model", {})
        training_report = result.get("entry_policy_training_report", {})
        decision_model = decision_result.get("decision_policy_model", {})
        decision_training_report = decision_result.get("decision_policy_training_report", {})
        if not isinstance(entry_model, dict) or not isinstance(training_report, dict):
            raise SystemExit(f"Training failed for candidate {name}.")
        if not isinstance(decision_model, dict) or not isinstance(decision_training_report, dict):
            raise SystemExit(f"Decision-policy training failed for candidate {name}.")

        bundle_payload = copy.deepcopy(base_bundle)
        bundle_payload["entry_policy_model"] = entry_model
        bundle_payload["entry_policy_training_report"] = training_report
        bundle_payload["decision_policy_model"] = decision_model
        bundle_payload["decision_policy_training_report"] = decision_training_report
        meta = bundle_payload.setdefault("metadata", {})
        if isinstance(meta, dict):
            meta["entry_policy_retrained_at_utc"] = datetime.now(timezone.utc).isoformat()
            meta["entry_policy_retrained_from_decisions_csv"] = str(decisions_csv_path)
            meta["entry_policy_retrained_from_trade_attribution_csv"] = str(trade_csv_path)
            meta["entry_policy_retrained_base_bundle"] = str(base_bundle_path)
            meta["entry_policy_candidate_name"] = name
            meta["decision_policy_retrained_at_utc"] = datetime.now(timezone.utc).isoformat()
            meta["decision_policy_retrained_from_decisions_csv"] = str(decisions_csv_path)
            meta["decision_policy_retrained_from_trade_attribution_csv"] = str(trade_csv_path)

        bundle_path = output_dir / f"dynamic_engine3_v4_bundle.{name}.json"
        report_path = output_dir / f"{name}.training_report.json"
        decision_report_path = output_dir / f"{name}.decision_training_report.json"
        bundle_path.write_text(json.dumps(bundle_payload, indent=2, ensure_ascii=True), encoding="utf-8")
        report_path.write_text(json.dumps(training_report, indent=2, ensure_ascii=True), encoding="utf-8")
        decision_report_path.write_text(
            json.dumps(decision_training_report, indent=2, ensure_ascii=True),
            encoding="utf-8",
        )

        selected_metrics = training_report.get("selected_tune_metrics", {}) if isinstance(training_report.get("selected_tune_metrics"), dict) else {}
        baseline_metrics = training_report.get("baseline_tune_metrics", {}) if isinstance(training_report.get("baseline_tune_metrics"), dict) else {}
        decision_selected_metrics = (
            decision_training_report.get("selected_tune_metrics", {})
            if isinstance(decision_training_report.get("selected_tune_metrics"), dict)
            else {}
        )
        decision_baseline_metrics = (
            decision_training_report.get("baseline_tune_metrics", {})
            if isinstance(decision_training_report.get("baseline_tune_metrics"), dict)
            else {}
        )
        summary["candidates"][name] = {
            "bundle_path": str(bundle_path),
            "training_report_path": str(report_path),
            "decision_training_report_path": str(decision_report_path),
            "selected_threshold": entry_model.get("selected_threshold"),
            "selected_threshold_source": entry_model.get("selected_threshold_source"),
            "decision_selected_threshold": decision_model.get("selected_threshold"),
            "decision_selected_threshold_source": decision_model.get("selected_threshold_source"),
            "decision_policy_enabled": bool(decision_model.get("enabled", False)),
            "decision_selection_mode": decision_model.get("selection_mode"),
            "csv_audit": training_report.get("csv_audit"),
            "selected_tune_metrics": selected_metrics,
            "baseline_tune_metrics": baseline_metrics,
            "decision_selected_tune_metrics": decision_selected_metrics,
            "decision_baseline_tune_metrics": decision_baseline_metrics,
            "entry_score_components": entry_model.get("score_components"),
            "decision_score_components": decision_model.get("score_components"),
        }
        print(
            f"{name}: threshold={entry_model.get('selected_threshold')} "
            f"keep={selected_metrics.get('keep_trades')} "
            f"pf={selected_metrics.get('profit_factor')} "
            f"net={selected_metrics.get('net_pnl')} "
            f"dd={selected_metrics.get('max_drawdown')}"
        )

    summary_path = output_dir / "candidate_summary.json"
    summary_path.write_text(json.dumps(summary, indent=2, ensure_ascii=True), encoding="utf-8")
    print(f"candidate_summary={summary_path}")


if __name__ == "__main__":
    main()
